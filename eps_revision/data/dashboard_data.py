# ── 유니버스 CSV 로딩 ─────────────────────────────────────────────────────────
def _load_universe() -> pd.DataFrame:
    csv_path = Path(__file__).parent / "data" / "종목_유니버스_260628.csv"
    df = pd.read_csv(csv_path, dtype=str, encoding="utf-8-sig")
    df.columns = df.columns.str.strip()
    df = df[["종목코드", "종목명", "시가총액", "섹터"]].copy()
    df = df.dropna(subset=["종목코드", "섹터"])
    df["종목코드"] = df["종목코드"].str.strip().str.zfill(6)
    # 알파벳 포함 특수코드 제외 (pykrx 조회 불가)
    df = df[df["종목코드"].str.match(r"^\d{6}$")]
    df["시가총액"] = pd.to_numeric(df["시가총액"], errors="coerce").fillna(0)
    df["mkt_억"] = (df["시가총액"] / 1e8).astype(int)
    return df.reset_index(drop=True)


_UNI = _load_universe()


def _make_cos(sector_name: str) -> list[dict]:
    """섹터명 기준으로 해당 섹터 종목 리스트 생성."""
    rows = _UNI[_UNI["섹터"] == sector_name].copy()
    # 시가총액 내림차순 정렬
    rows = rows.sort_values("mkt_억", ascending=False)

    cos = []
    for _, row in rows.iterrows():
        ticker = row["종목코드"]
        name   = row["종목명"]
        mkt    = int(row["mkt_억"])

        if ticker in _LEGACY:
            # 기존 상세 데이터 사용
            leg = _LEGACY[ticker]
            cos.append({
                "t": ticker, "n": name,
                "sc":  leg["sc"],
                "ev":  leg["ev"],
                "bRev": leg["bRev"],
                "bOp":  leg["bOp"],
                "mkt":  leg["mkt"],
                "p":    leg["p"],
                "pc":   leg["pc"],
                "br":   leg["br"],
            })
        else:
            # 신규 종목 — 시가총액 기반 더미 데이터
            b_rev = max(100, int(mkt * 0.12))
            b_op  = max(5,   int(b_rev * 0.08))
            cos.append({
                "t": ticker, "n": name,
                "sc":  {"e": 20, "d": 15, "s": 12},
                "ev":  [],
                "bRev": b_rev,
                "bOp":  b_op,
                "mkt":  mkt,
                "p":    0,      # FnSpace API 연동 후 채울 것
                "pc":   0.0,
                "br":   1.0,
            })
    return cos


# ── SECTORS 동적 빌드 ─────────────────────────────────────────────────────────
