# ── 데이터 생성기 ─────────────────────────────────────────────────────────────
def gen_fin(b_rev: float, b_op: float, sd: int) -> list[dict]:
    r = seed_rand(sd)
    out = []
    for i, q in enumerate(QQ):
        rev = round(b_rev * (0.76 + i * 0.046 + r() * 0.055))
        op  = max(0, round(b_op * (0.48 + i * 0.074 + r() * 0.065)))
        opm = max(0, round(op / rev * 100)) if rev > 0 else 0
        out.append({"q": q, "rev": rev, "op": op, "opm": opm})
    return out


def gen_cons(base: float, sd: int) -> list[dict]:
    r = seed_rand(sd)
    v1, v2 = base * 0.86, base * 1.03
    out = []
    for m in M12:
        v1 = round(v1 * (1 + (r() - 0.38) * 0.025))
        v2 = round(v2 * (1 + (r() - 0.36) * 0.025))
        out.append({"m": m, "fy1": v1, "fy2": v2})
    return out


def gen_score(base: float, sd: int) -> list[dict]:
    r = seed_rand(sd)
    v = float(base - 14)
    out = []
    for m in M12:
        v = min(97.0, max(18.0, v + (r() - 0.4) * 5 + 1.1))
        out.append({"m": m, "score": round(v)})
    return out


def gen_short(base: float, sd: int) -> list[dict]:
    r = seed_rand(sd)
    b = round(base * 0.84)
    out = []
    for m in M9:
        b = round(b * (1.012 + r() * 0.022))
        ratio = round(b / base * 0.1, 2) if base > 0 else 0.0
        out.append({"m": m, "bal": b, "ratio": ratio})
    return out


def gen_export(base: float, sd: int) -> list[dict]:
    r = seed_rand(sd)
    out = []
    for i, m in enumerate(M9):
        val = round(base * (0.84 + i * 0.022 + r() * 0.075))
        yoy = round(-7 + i * 3.4 + r() * 8)
        out.append({"m": m, "val": val, "yoy": yoy})
    return out


def gen_spread(sd: int) -> list[dict]:
    r = seed_rand(sd)
    v = 0.0
    out = []
    for m in M9:
        v += (r() - 0.43) * 2.8
        out.append({"m": m, "spread": round(v, 1)})
    return out


