# ── Seed-based RNG (Park-Miller LCG, JS seedRand 동일) ──────────────────────
def seed_rand(seed: int):
    s = abs(int(seed or 1) % 2147483647) or 1
    def _next() -> float:
        nonlocal s
        s = (s * 16807) % 2147483647
        return (s - 1) / 2147483646.0
    return _next


